        <h3>§ 14 Verlängerung der Beherbergung</h3>
        <p>14.1 Der Vertragspartner hat keinen Anspruch darauf, dass sein Aufenthalt verlängert wird. Kündigt der
          Vertragspartner seinen Wunsch auf Verlängerung des Aufenthalts rechtzeitig an, so kann der Beherberger der
