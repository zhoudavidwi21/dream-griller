          Abreisemöglichkeiten gesperrt oder nicht benutzbar sind, so wird der Beherbergungsvertrag für die Dauer der
          Unmöglichkeit der Abreise automatisch verlängert. Eine Reduktion des Entgelts für diese Zeit ist allenfalls
          nur dann möglich, wenn der Vertragspartner die angebotenen Leistungen des Beherbergungsbetriebes infolge der
          außergewöhnlichen Witterungsverhältnisse nicht zur Gänze nutzen kann. Der Beherberger ist berechtigt
          mindestens jenes Entgelt zu begehren, das dem gewöhnlich verrechneten Preis in der Nebensaison entspricht.</p>
