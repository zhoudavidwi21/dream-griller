        <p>16.1 Erkrankt ein Gast während seines Aufenthaltes im Beherbergungsbetrieb, so wird der Beherberger über
          Wunsch des Gastes für ärztliche Betreuung sorgen. Ist Gefahr in Verzug, wird der Beherberger die ärztliche
          Betreuung auch ohne besonderen Wunsch des Gastes veranlassen, dies insbesondere dann, wenn dies notwendig ist
          und der Gast hiezu selbst nicht in der Lage ist.</p>
        <p>16.2 Solange der Gast nicht in der Lage ist, Entscheidungen zu treffen oder die Angehörigen des Gastes nicht
          kontaktiert werden können, wird der Beherberger auf Kosten des Gasten für ärztliche Behandlung sorgen. Der
