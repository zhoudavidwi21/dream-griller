        <p>3.1 Der Beherbergungsvertrag kommt durch die Annahme der Bestellung des Vertragspartners durch den
          Beherberger zustande. Elektronische Erklärungen gelten als zugegangen, wenn die Partei, für die sie bestimmt
          sind, diese unter gewöhnlichen Umständen abrufen kann, und der Zugang zu den bekannt gegebenen Geschäftszeiten
          des Beherbergers erfolgt.</p>
        <p>3.2 Der Beherberger ist berechtigt, den Beherbergungsvertrag unter der Bedingung abzuschließen, dass der
          Vertragspartner eine Anzahlung leistet. In diesem Fall ist der Beherberger verpflichtet, vor der Annahme der
          schriftlichen oder mündlichen Bestellung des Vertragspartners, den Vertragspartner auf die geforderte
          Anzahlung hinzuweisen. Erklärt sich der Vertragspartner mit der Anzahlung (schriftlich oder mündlich)
          einverstanden, kommt der Beherbergungsvertrag mit Zugang der Einverständniserklärung über die Bezahlung der
          Anzahlung des Vertragspartners beim Beherberger zustande.</p>
        <p>3.3 Der Vertragspartner ist verpflichtet, die Anzahlung spätestens 7 Tage (einlangend) vor der Beherbergung
          zu bezahlen. Die Kosten für die Geldtransaktion (zB Überweisungsspesen) trägt der Vertragspartner. Für Kredit-
          und Debitkarten gelten die jeweiligen Bedingungen der Kartenunternehmen.</p>
        <p>3.4 Die Anzahlung ist eine Teilzahlung auf das vereinbarte Entgelt.</p>
