        <h3>§ 18 Sonstiges</h3>
        <p>18.1 Sofern die obigen Bestimmungen nichts Besonderes vorsehen, beginnt der Lauf einer Frist mit Zustellung
          des die Frist anordnenden Schriftstückes an die Vertragspartner, welche die Frist zu wahren hat. Bei
          Berechnung einer Frist, welche nach Tagen bestimmt ist, wird der Tag nicht mitgerechnet, in welchen der
          Zeitpunkt oder die Ereignung fällt, nach der sich der Anfang der Frist richten soll. Nach Wochen oder Monaten
          bestimmte Fristen beziehen sich auf denjenigen Tage der Woche oder des Monates, welcher durch seine Benennung
          oder Zahl dem Tage entspricht, von welchem die Frist zu zählen ist. Fehlt dieser Tag in dem Monat, ist der in
          diesem Monat letzte Tag maßgeblich.</p>
        <p>18.2 Erklärungen müssen dem jeweils anderen Vertragspartner am letzten Tag der Frist (24 Uhr) zugegangen
          sein.</p>
        <p>18.3 Der Beherberger ist berechtigt, gegen Forderung des Vertragspartners mit eigenen Forderungen
          aufzurechnen. Der Vertragspartner ist nicht berechtigt mit eigenen Forderungen gegen Forderungen des
          Beherbergers aufzurechnen, es sei denn, der Beherberger ist zahlungsunfähig oder die Forderung des
          Vertragspartners ist gerichtlich festgestellt oder vom Beherberger anerkannt.</p>
        <p>18.4 Im Falle von Regelungslücken gelten die entsprechenden gesetzlichen Bestimmungen.</p>
