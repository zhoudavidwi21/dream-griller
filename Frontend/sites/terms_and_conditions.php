        <p>4.1 Der Vertragspartner hat das Recht, so der Beherberger keine andere Bezugszeit anbietet, die gemieteten
          Räume ab 16.00 Uhr des vereinbarten Tages („Ankunftstag“) zu beziehen.</p>
