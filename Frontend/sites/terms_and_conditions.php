          Ersparnis liegt nur dann vor, wenn der Beherbergungsbetrieb im Zeitpunkt der Nichtinanspruchnahme der vom Gast
          bestellten Räumlichkeiten vollständig ausgelastet ist und die Räumlichkeit auf Grund der Stornierung des
          Vertragspartners an weitere Gäste vermietet werden kann. Die Beweislast der Ersparnis trägt der
          Vertragspartner.</p>
        <p>15.3 Durch den Tod eines Gastes endet der Vertrag mit dem Beherberger.</p>
        <p>15.4 Wurde der Beherbergungsvertrag auf unbestimmte Zeit abgeschlossen, so können die Vertragsparteien den
          Vertrag, bis 10.00 Uhr des dritten Tages vor dem beabsichtigten Vertragsende, auflösen.</p>
        <p>15.5 Der Beherberger ist berechtigt, den Beherbergungsvertrag mit sofortiger Wirkung aus wichtigem Grund
