        <h3>§ 17 Erfüllungsort, Gerichtsstand und Rechtswahl</h3>
        <p>17.1 Erfüllungsort ist der Ort, an dem der Beherbergungsbetrieb gelegen ist.</p>
        <p>17.2 Dieser Vertrag unterliegt österreichischem formellen und materiellen Recht unter Ausschluss der Regeln
          des Internationalen Privatrechts (insb IPRG und EVÜ) sowie UN-Kaufrecht.</p>
        <p>17.3 Ausschließlicher Gerichtsstand ist im zweiseitigen Unternehmergeschäft der Sitz des Beherbergers, wobei
          der Beherberger überdies berechtigt ist, seine Rechte auch bei jedem anderem örtlichem und sachlich
          zuständigem Gericht geltend zu machen.</p>
        <p>17.4 Wurde der Beherbergungsvertrag mit einem Vertragspartner, der Verbraucher ist und seinen Wohnsitz bzw
          gewöhnlichen Aufenthalt in Österreich hat, geschlossen, können Klagen gegen den Verbraucher ausschließlich am
          Wohnsitz, am gewöhnlichen Aufenthaltsort oder am Beschäftigungsort des Verbrauchers eingebracht werden.</p>
        <p>17.5 Wurde der Beherbergungsvertrag mit einem Vertragspartner, der Verbraucher ist und seinen Wohnsitz in
          einem Mitgliedsstaat der Europäischen Union (mit Ausnahme Österreichs), Island, Norwegen oder der Schweiz,
